package UD2;

public class SecuenciasEscape {
    public static void main(String[] args) {
        System.out.println("Tengo una \\n \n en medio");
        System.out.println("Tengo una \\b \b en medio");
        System.out.println("Tengo una \\t \t en medio");
        System.out.println("Tengo una \\r \r en medio");
        System.out.println("Tengo una \\\" \" en medio");
        System.out.println("Tengo una \\\' \' y una sin \\ ' en medio");
        System.out.println("Tengo un unicode \u56e3 en medio");
    }
}

