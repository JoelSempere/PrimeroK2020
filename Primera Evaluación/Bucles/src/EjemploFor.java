public class EjemploFor {
  public static void main(String[] args) {
    //El for tiene inicialización, comprobación y actualización
    //separados por ;

    //El for se usa cuando sabemos antes de entrar en el bucle
    //la vueltas (iteraciones) que voy a dar
    //La i no se cambia dentro del for
    for(int i=0;i<20;i++){
      System.out.println(i+1);
    }
  }
}
